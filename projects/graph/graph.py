from util import Stack, Queue  # These may come in handy


class Graph:
    """Represent a graph as a dictionary of vertices mapping labels to edges."""

    def __init__(self):
        self.vertices = {}

    def add_vertex(self, vertex):
        """
        Add a vertex to the graph.
        """
        self.vertices[vertex] = set()

    def add_edge(self, v1, v2):
        """
        Add a directed edge to the graph.
        """
        if v1 in self.vertices and v2 in self.vertices:
            self.vertices[v1].add(v2)
            self.vertices[v2].add(v1)
        else:
            raise IndexError("This vertex doesn't exist")

    def bft(self, starting_vertex):
        # create an empty queue
        q = Queue()
        # create a set to store the nodes visited
        visited = set()
        # add starting_vertex to the queue
        q.enqueue(starting_vertex)
        # iterate through each vertex while the queue is not empty
        while q.size() > 0:
            # dequeue the first vertex in the queue
            current = q.dequeue()
            # if the vertex is not visited, visit it and add it to the set
            if current not in visited:
                visited.add(current)
            # add its neighbors to the queue
                for next_vert in self.vertices[current]:
                    q.enqueue(next_vert)

    def dft(self, starting_vertex):
        s = Stack()
        visited = set()
        s.push(starting_vertex)

        while s.size() > 0:
            current = s.pop()
            if current not in visited:
                visited.add(current)
                for next_vert in self.vertices[current]:
                    s.push(next_vert)

    def dft_recursive(self, starting_vertex, visited=None):
        """
        Print each vertex in depth-first order
        beginning from starting_vertex.
        This should be done using recursion.
        """
        if visited is None:
            visited = set()
        visited.add(starting_vertex)
        print(starting_vertex)
        for child_vert in self.vertices[starting_vertex]:
            if child_vert not in visited:
                self.dft_recursive(child_vert, visited)

    def bfs(self, starting_vertex, destination_vertex):
        """
        Return a list containing the shortest path from
        starting_vertex to destination_vertex in
        breath-first order.
        """
        q = Queue()
        visited = set()
        q.enqueue([starting_vertex])
        while q.size() > 0:
            path = q.dequeue()
        #     if current in visited:
        #         continue
        #     if current == destination_vertex:
        #         return f"{current} its me"
        #     for vert in self.vertices[current]:
        #         q.enqueue(vert)
        #     visited.add(current)
            current = path[-1]
            if current == destination_vertex:
                return "BFS: " + str(path)
            if current not in visited:
                visited.add(current)
                for neighbor in self.vertices[current]:
                    path_copy = list(path)
                    path_copy.append(neighbor)
                    q.enqueue(path_copy)
        return "BFS: Value not found"

    def dfs(self, starting_vertex, destination_vertex):
        """
        Return a list containing a path from
        starting_vertex to destination_vertex in
        depth-first order.
        """
        s = Stack()
        visited = set()
        s.push([starting_vertex])
        while s.size() > 0:
            path = s.pop()
            current = path[-1]
            if current == destination_vertex:
                return "DFS: " + str(path)
            if current not in visited:
                visited.add(current)
                for neighbor in self.vertices[current]:
                    path_copy = list(path)
                    path_copy.append(neighbor)
                    s.push(path_copy)
        return "DFS: Value not found"


